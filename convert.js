let value = 80;

if (value >= 90) {
  console.log("Congrat you grade is 'A'");
} else if (value >= 80) {
  console.log("Congrat your grade is 'B'");
} else if (value >= 70) {
  console.log("Congrat your grade is 'C'");
} else if (value >= 60) {
  console.log("Sorry, you didn't pass.");
} else {
  console.log("Sorry, you failed.");
}
